package entity;

/**
 *
 * @author EGE
 */
public class Horoscope {

    private int id;
    private String name;

    public Horoscope(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Horoscope() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String ad) {
        this.name = ad;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 41 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Horoscope other = (Horoscope) obj;
        return this.id == other.id;
    }

}
