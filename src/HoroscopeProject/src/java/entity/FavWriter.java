package entity;

/**
 *
 * @author EGE
 */
public class FavWriter {

    private int id;
    private Reader reader;
    private Writer writer;

    public FavWriter() {
    }

    public FavWriter(int id, Reader reader, Writer writer) {
        this.id = id;
        this.reader = reader;
        this.writer = writer;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Reader getReader() {
        return reader;
    }

    public void setReader(Reader reader) {
        this.reader = reader;
    }

    public Writer getWriter() {
        return writer;
    }

    public void setWriter(Writer writer) {
        this.writer = writer;
    }

}
